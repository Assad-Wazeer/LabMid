package observerpattern;

/**
 *
 * @author Global Computer
 */
public class User {
    private String id;
    private String name;
    private String role; // "student" or "faculty"

    public User(String id, String name, String role) {
        this.id = id;
        this.name = name;
        this.role = role;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getRole() {
        return role;
    }
}

